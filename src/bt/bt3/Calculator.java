package bt.bt3;

public class Calculator {
    public static double multiplication(double a, double b) {
        return a * b;
    }

    public static double division(double a, double b) {return a / b;
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }
}
